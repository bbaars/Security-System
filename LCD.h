#ifndef LCD_H_
#define LCD_H_

/******************************************************************************
 * This Function initializes MCLK to 48Mhz frequency for the LCD to Function
 *****************************************************************************/
extern void LCD_ClockInit48Mhz(void);

/******************************************************************************
 * Updates the time and temperature on the LCD Display
 *****************************************************************************/
extern void LCD_updateData(void);

/******************************************************************************
 * This function updates the user with the status of the secruity alarms
 *****************************************************************************/
extern void LCD_updateSecurityStatus(uint8_t unit);

/******************************************************************************
 * Shows the user the main screen which allows them to change various things
 * about their system
 *****************************************************************************/
extern void LCD_showMenu(void);

/*******************************************************************************
 * Goes back to showing the state of the system
 ******************************************************************************/
extern void LCD_displayHome(void);

/******************************************************************************
 * Shows the last 5 alarm trips and where they were from
 *****************************************************************************/
extern void LCD_showAlarm(void);

/******************************************************************************
 * Changes the date and time
 *****************************************************************************/
extern void LCD_changeDateTime(void);

/******************************************************************************
 * Changes the user pin
 *****************************************************************************/
extern void LCD_changePin(void);

/*******************************************************************************
 * Displays the splash screen/startup screen
 ******************************************************************************/
extern void LCD_startup(void);

/******************************************************************************
 * Updates the screen to display the entered PIN
 *****************************************************************************/
extern void LCD_pin(void);

/******************************************************************************
 * Customizes the screen for the user
 ******************************************************************************/
extern void LCD_customize(void);

/*******************************************************************************
 * This function locks and unlocks the door
 ********************************************************************************/
extern void LCD_motor(void);

/******************************************************************************
 * Sets the users intital pin
 *****************************************************************************/
void LCD_setInitPin(void);


#endif /* LCD_H_ */
